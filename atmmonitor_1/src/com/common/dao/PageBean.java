package com.common.dao;

public class PageBean {

	private int page; //当前第几页
	private int pagesize;// 每页行数
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getPagesize() {
		return pagesize;
	}
	public void setPagesize(int pagesize) {
		this.pagesize = pagesize;
	}
	
	
	
}
